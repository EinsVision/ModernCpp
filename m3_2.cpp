#include "projects.h"

void Projects_3::m3_2()
{
	// 3.2 산술연산자 Arithmetic Operators
	double x = 1.0;
	double y = 2.0;
	double z = 0.0;
	z = x + y;
	cout << " z = x + y \t" << z << endl;
	z = x - y;
	cout << " z = x - y \t" << z << endl;
	z = x * y;
	cout << " z = x * y \t" << z << endl;
	z = x / y;
	cout << " z = x / y \t" << z << endl;
	
	z += x;
	cout << " z += x \t" << z << endl;

}
